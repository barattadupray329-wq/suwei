import { asc, eq } from 'drizzle-orm'
import { db } from '@/lib/db'
import { websitePackages } from '@/lib/db/schema'

export async function getPublicWebsitePackages() {
  return db.select().from(websitePackages).where(eq(websitePackages.active, true)).orderBy(asc(websitePackages.sortOrder), asc(websitePackages.id))
}
