import type { Metadata } from 'next'
import { headers } from 'next/headers'
import { redirect } from 'next/navigation'
import { getWebsitePackagesForAdmin } from '@/app/actions/business'
import { auth } from '@/lib/auth'
import { WebsitePackageManagement } from '@/components/website-package-management'

export const metadata: Metadata = { title: '官网套餐管理 | 速维租赁' }
export const dynamic = 'force-dynamic'

export default async function WebsitePackagesPage() {
  const session = await auth.api.getSession({ headers: await headers() })
  if (!session?.user) redirect('/sign-in')
  return <WebsitePackageManagement packages={await getWebsitePackagesForAdmin()} />
}
